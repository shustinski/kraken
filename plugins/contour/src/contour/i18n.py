def tr(key: str, default: str = "", language: str | None = None, **kwargs: Any) -> str:
    lang = active_language(language)
    value = TRANSLATIONS.get(lang, {}).get(key)
    if value is None:
        value = TRANSLATIONS["en"].get(key) or TRANSLATIONS["ru"].get(key) or default or key
    if kwargs:
        return value.format(**kwargs)
    return value


